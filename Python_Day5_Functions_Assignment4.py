#4.Accept a name from the user and display that in lower case using lower() function.

#Getting name from the User
Name = input("Enter Your Name: ")

#Converting the name into LowerCase
lower_Case = Name.lower()

#Displaye the name in LowerCase
print("Your Name in LowerCase is: ",lower_Case)
      
"""Ans: Enter Your Name: JANHAVI
Your Name in LowerCase is:  janhavi"""
