"""1. Write a Python program to find the number of times 4 appears in the tuple. 
Input: tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 ) Output: 3 """

# Given tuple
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7)

# Count the occurrences of 4
count_of_4 = tuplex.count(4)

# Print the result
print(f"The number 4 appears {count_of_4} times in the tuple.")

#Ans:The number 4 appears 3 times in the tuple.

