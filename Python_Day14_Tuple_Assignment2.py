"""2.Write a Python program to convert a list to a tuple.
Input: listx = [5, 10, 7, 4, 15, 3] Output: (5, 10, 7, 4, 15, 3)"""

# Given list
listx = [5, 10, 7, 4, 15, 3]

# Convert list to tuple
tuplex = tuple(listx)

# Print the result
print(f"The tuple is: {tuplex}")

#Ans: The tuple is: (5, 10, 7, 4, 15, 3)
